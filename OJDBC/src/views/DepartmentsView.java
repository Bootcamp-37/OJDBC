    private void loadData(List<Department> dataDepartements) {
        String header[] = {"Department ID","Department Name","Manager ID","Location ID"};
        String data[][] = new String[dataDepartements.size()][4];
        for (int i = 0; i < data.length ; i++) {
            data[i][0] = dataDepartements.get(i).getDept_id()+"";
            data[i][1] = dataDepartements.get(i).getDept_name();
            data[i][2] = dataDepartements.get(i).getManager_id()+"";
            data[i][3] = dataDepartements.get(i).getLoc_id()+"";
            
        }
        DefaultTableModel tableModel = new DefaultTableModel(data, header);
        tblDepartments.setModel(tableModel);
        
                
    }


