    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        int confirm = JOptionPane.showConfirmDialog(this,"Konfirmasi Hapus ?", "Delete", JOptionPane.YES_NO_CANCEL_OPTION);
        String dept_id = txtDepartment_ID.getText();
        if (confirm == 0) {
            String message = this.controller.deleteDepartmentSP(dept_id);
            JOptionPane.showMessageDialog(this, message);
            }
        loadData(controller.getDataDepartements());
        txtDepartment_ID.setText(null);
        txtDepartment_Name.setText(null);
        txtManager_ID.setText(null);
        txtLocation_ID.setText(null);
        
    }//GEN-LAST:event_btnDeleteActionPerformed

