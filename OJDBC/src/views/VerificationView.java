    private void txtVerificationKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtVerificationKeyPressed
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            String code = txtVerification.getText();
            String newId = id+"";
            boolean verifResult = userController.verficationUser(code,newId);
            if (verifResult) {
                JOptionPane.showMessageDialog(this, "Verfikasi akun berhasil");
                if ( id != 0) {
                    NewPasswordView newPassword = new NewPasswordView();
                    newPassword.show();
                    this.getDesktopPane().add(newPassword);
                }
                this.dispose();
            }else{
                JOptionPane.showMessageDialog(this, "Kode salah. Masukkan code yang tepat");
            }
        }
    }//GEN-LAST:event_txtVerificationKeyPressed


