    private void btnSendActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSendActionPerformed
        String code = txtVerification.getText();
        String newId = id+"";
        if (CheckCode(code,newId)) {
            JOptionPane.showMessageDialog(this, "Verfikasi akun berhasil");
            if (prevView) {
                NewPasswordView newPassword = new NewPasswordView(newId,mnJob, mnRegion);
                newPassword.show();
                this.getDesktopPane().add(newPassword);
            }else{
                LoginView view = new LoginView(mnJob, mnRegion);
                view.show();
                this.getDesktopPane().add(view);
            }
            this.dispose();
        }else{
            JOptionPane.showMessageDialog(this, "Kode salah. Masukkan code yang tepat");
        }
    }//GEN-LAST:event_btnSendActionPerformed

